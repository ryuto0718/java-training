public class Main{
    public static void main(String[] args){
        int x = 10;
        int y = 5;
        System.out.println("x=" + x + ",y=" + y);
        if(x > y){
            System.out.println("xはyよりも大きい");
        }
        if(x == y){
            System.out.println("xとyは等しい");
        }
        if(x < y){
            System.out.println("xはyよりも小さい");
        }
    }
}